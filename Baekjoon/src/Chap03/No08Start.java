package Chap03;
import java.util.*;

public class No08Start {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		int N = sc.nextInt();
		
		
		for(int i = N; i<0; i+=N){
			for(int j =N; j<=0; j++){
				System.out.print("*");
		  	}
		 System.out.println();
		 }
	

	}

}
